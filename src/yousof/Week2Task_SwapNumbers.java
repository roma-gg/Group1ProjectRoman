package yousof;

public class Week2Task_SwapNumbers {

    public static void main(String[] args) {

     int n1 = 20;
     int n2 = 25;

     n1 = n1 + n2;
     n2 = n1 - n2;
     n1 = n1 - n2;

     System.out.println("n1 = " + n1);
     System.out.println("n2 = " + n2);

    }

}
